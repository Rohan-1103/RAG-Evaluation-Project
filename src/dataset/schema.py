def make_dataset_id() -> str:
    """Generate a ULID-based dataset ID. Format: 'ds_{ulid}'."""
    return f"ds_{_generate_ulid()}"


def make_pair_id() -> str:
    """Generate a ULID-based QAPair ID. Format: 'qap_{ulid}'."""
    return f"qap_{_generate_ulid()}"


def _generate_ulid() -> str:
    """
    Generate a ULID (Universally Unique Lexicographically Sortable ID).

    Falls back to timestamp-prefixed UUID4 if ulid-py is not installed.
    The fallback maintains the sortable property via timestamp prefix.
    """
    try:
        import ulid
        return str(ulid.new())
    except ImportError:
        import uuid
        ts = int(datetime.now(timezone.utc).timestamp() * 1000)
        return f"{ts:013x}{uuid.uuid4().hex[:13]}"


def create_eval_dataset(
    name: str,
    pairs: list[QAPair],
    source_collection: str | None = None,
    source_files: list[str] | None = None,
    generator_model: str | None = None,
    generation_method: GenerationMethod = GenerationMethod.SYNTHETIC,
    description: str | None = None,
    tags: list[str] | None = None,
) -> EvalDataset:
    """
    Factory function for creating a new EvalDataset.

    Handles ID generation, timestamp setting, and metadata
    construction so callers never need to build DatasetMetadata
    manually.

    Usage:
        dataset = create_eval_dataset(
            name="Q3 Financial Report Eval",
            pairs=generated_pairs,
            source_collection="q3_report",
            source_files=["q3_report.pdf"],
            generator_model="gemini-1.5-flash",
        )
    """
    now = datetime.now(timezone.utc)
    dataset_id = make_dataset_id()

    # Stamp dataset_id on all pairs
    for pair in pairs:
        pair.dataset_id = dataset_id

    metadata = DatasetMetadata(
        id=dataset_id,
        name=name,
        description=description,
        created_at=now,
        updated_at=now,
        generation_method=generation_method,
        source_collection=source_collection,
        source_files=source_files or [],
        generator_model=generator_model,
        total_pairs=len(pairs),
        status=DatasetStatus.READY if pairs else DatasetStatus.DRAFT,
        tags=tags or [],
    )

    return EvalDataset(metadata=metadata, pairs=pairs)


