class DatasetMetadata(BaseModel):
    """
    Metadata about an EvalDataset — stored alongside QAPairs.

    Separate from EvalDataset so the UI can display the list of
    datasets (name, count, status) without loading all QAPairs.
    """

    model_config = ConfigDict(frozen=True)

    id: str
    name: str
    description: str | None = None
    created_at: datetime
    updated_at: datetime
    generation_method: GenerationMethod
    source_collection: Annotated[
        str | None,
        Field(
            default=None,
            description=(
                "ChromaDB collection name this dataset was generated from. "
                "None for manually created datasets."
            ),
        ),
    ]
    source_files: list[str] = Field(
        default_factory=list,
        description="List of source filenames used in generation.",
    )
    generator_model: str | None = Field(
        default=None,
        description="Model used to generate Q&A pairs (e.g. gemini-1.5-flash).",
    )
    total_pairs: int = Field(ge=0)
    status: DatasetStatus = Field(default=DatasetStatus.DRAFT)
    tags: list[str] = Field(default_factory=list)
    version: str = Field(
        default="1.0.0",
        description="Dataset version for change tracking.",
    )

    @property
    def is_ready_for_eval(self) -> bool:
        """True if dataset can be used in an evaluation run."""
        return (
            self.status in (DatasetStatus.READY, DatasetStatus.PARTIAL)
            and self.total_pairs > 0
        )

    def __repr__(self) -> str:
        return (
            f"DatasetMetadata("
            f"id='{self.id}', "
            f"name='{self.name}', "
            f"pairs={self.total_pairs}, "
            f"status={self.status.value})"
        )


