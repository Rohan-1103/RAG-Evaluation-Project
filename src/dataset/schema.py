"""
src/dataset/schema.py

Pydantic schemas for the test dataset layer.

These schemas are the universal data contract between:
  - GeminiDatasetGenerator  (produces QAPairs)
  - DatasetStore            (persists/loads EvalDatasets)
  - EvaluationEngine        (consumes EvalDatasets)
  - Streamlit UI            (displays and edits datasets)
  - RAGPipeline             (runs questions through RAG)

Design rules:
  - Every schema is frozen (immutable after construction).
  - Every schema is fully serialisable to/from JSON.
  - No schema imports from src/rag, src/evaluation, or src/vectorstore.
    The dataset layer is a pure data layer — zero coupling to
    the layers that consume it.
  - IDs are ULIDs (sortable by creation time) not UUID4s.
    ULID lexicographic order == chronological order — this means
    `ORDER BY dataset_id` in SQLite returns newest-last for free.

QAPair lifecycle:
    Generator creates QAPair with status="pending"
        ↓
    RAGPipeline runs the question → sets generated_answer, retrieved_chunks
        ↓
    EvaluationEngine scores it → sets metric_scores, status="evaluated"
        ↓
    DatasetStore persists the final EvalDataset to JSON

The same QAPair schema covers all three stages. Fields are Optional
where not yet populated — a QAPair with status="pending" has
generated_answer=None. This is intentional: one schema, one lifecycle.
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from enum import Enum
from pathlib import Path
from typing import Annotated, Any

from pydantic import (
    BaseModel,
    ConfigDict,
    Field,
    field_validator,
    model_validator,
)


# ===========================================================================
# ENUMS
# ===========================================================================


class QAPairStatus(str, Enum):
    """
    Lifecycle status of a single Q&A pair.

    Transitions:
        pending → answered → evaluated
        pending → failed (if RAG pipeline errors)
        answered → failed (if evaluation errors)

    Terminal states: evaluated, failed.
    Only evaluated pairs contribute to metric aggregation.
    """

    PENDING = "pending"          # Generated, not yet run through RAG
    ANSWERED = "answered"        # RAG answer generated, not yet evaluated
    EVALUATED = "evaluated"      # All 4 metric scores computed
    FAILED = "failed"            # Error at any stage — see error_message


class DatasetStatus(str, Enum):
    """
    Lifecycle status of an entire EvalDataset.

    draft      — being built or edited, not ready for eval
    ready      — all QAPairs are pending, ready for a run
    running    — evaluation run in progress
    completed  — all QAPairs evaluated
    partial    — some QAPairs failed, rest evaluated
    """

    DRAFT = "draft"
    READY = "ready"
    RUNNING = "running"
    COMPLETED = "completed"
    PARTIAL = "partial"


class GenerationMethod(str, Enum):
    """How this dataset was created."""

    SYNTHETIC = "synthetic"      # Auto-generated by GeminiDatasetGenerator
    MANUAL = "manual"            # Hand-authored by the user
    IMPORTED = "imported"        # Loaded from external CSV/JSON
    MIXED = "mixed"              # Combination of above


# ===========================================================================
# METRIC SCORES
# ===========================================================================


class MetricScore(BaseModel):
    """
    Score for a single evaluation metric on a single QAPair.

    Produced by one evaluator (FaithfulnessEvaluator, etc.)
    and stored on the QAPair. Four MetricScores per evaluated QAPair.

    The reasoning field is non-negotiable — a score without reasoning
    is undebuggable. The dashboard's drilldown table shows reasoning
    so users understand WHY a score was assigned.
    """

    model_config = ConfigDict(frozen=True)

    metric_name: Annotated[
        str,
        Field(
            description=(
                "Snake_case metric name: faithfulness, answer_relevance, "
                "context_precision, correctness."
            )
        ),
    ]

    score: Annotated[
        float,
        Field(
            ge=1.0,
            le=5.0,
            description="Score from 1.0 (worst) to 5.0 (best).",
        ),
    ]

    reasoning: Annotated[
        str,
        Field(
            min_length=10,
            description=(
                "Judge's step-by-step reasoning before the score. "
                "Minimum 10 chars — empty reasoning is a parse failure."
            ),
        ),
    ]

    prompt_version: Annotated[
        str,
        Field(
            description=(
                "Version of the judge prompt used. Stored so runs "
                "can be re-compared even after prompt updates."
            )
        ),
    ]

    judge_model: Annotated[
        str,
        Field(description="Model ID used as the judge for this score."),
    ]

    latency_ms: Annotated[
        float,
        Field(
            ge=0.0,
            description="Time taken for this single metric judge call.",
        ),
    ] = 0.0

    input_tokens: int = Field(default=0, ge=0)
    output_tokens: int = Field(default=0, ge=0)
    parse_failed: bool = Field(
        default=False,
        description=(
            "True if the judge response could not be parsed as valid JSON. "
            "Score is the fallback value from eval.yaml."
        ),
    )
    low_confidence: bool = Field(
        default=False,
        description=(
            "True if judge reasoning was shorter than min_reasoning_length. "
            "Score may be less reliable."
        ),
    )

    @property
    def estimated_cost_usd(self) -> float:
        """Placeholder — actual cost computed by cost estimator."""
        return 0.0

    def __repr__(self) -> str:
        return (
            f"MetricScore("
            f"metric={self.metric_name}, "
            f"score={self.score:.1f}, "
            f"parse_failed={self.parse_failed})"
        )


# ===========================================================================
# QA PAIR
# ===========================================================================


class QAPair(BaseModel):
    """
    A single question-answer pair in a test dataset.

    Central data object of the evaluation system.
    A QAPair starts as a (question, ground_truth_answer) pair and
    accumulates fields as it moves through the pipeline:
      1. Generated:  question + ground_truth_answer + source_chunk
      2. Answered:   + generated_answer + retrieved_chunks + latency
      3. Evaluated:  + metric_scores + composite_score + status=evaluated

    Immutability note:
      QAPair is NOT frozen because the pipeline mutates it in place
      (sets generated_answer, metric_scores, status).
      All mutations go through explicit methods (set_answer, set_scores)
      that validate the transition is legal.
    """

    model_config = ConfigDict(
        frozen=False,        # Mutable — see lifecycle note above
        validate_assignment=True,   # Validate fields on every assignment
    )

    # ------------------------------------------------------------------
    # Identity
    # ------------------------------------------------------------------

    id: Annotated[
        str,
        Field(
            description=(
                "Unique QAPair ID. Format: 'qap_{ulid}'. "
                "Lexicographically sortable by creation time."
            )
        ),
    ]

    dataset_id: Annotated[
        str,
        Field(description="ID of the EvalDataset this pair belongs to."),
    ]

    created_at: Annotated[
        datetime,
        Field(description="UTC timestamp of pair creation."),
    ]

    # ------------------------------------------------------------------
    # Source tracking
    # ------------------------------------------------------------------

    source_chunk: Annotated[
        str,
        Field(
            min_length=1,
            description=(
                "The document chunk this Q&A pair was generated from. "
                "Used to verify faithfulness during evaluation."
            ),
        ),
    ]

    source_file: Annotated[
        str,
        Field(
            description="Filename of the source document (e.g. 'policy.pdf')."
        ),
    ]

    source_page: Annotated[
        int | None,
        Field(
            default=None,
            description=(
                "Page number within the source document. "
                "None for formats without page structure (TXT, HTML)."
            ),
        ),
    ]

    chunk_index: Annotated[
        int | None,
        Field(
            default=None,
            ge=0,
            description="0-based chunk index within the source document.",
        ),
    ]

    # ------------------------------------------------------------------
    # Core Q&A content
    # ------------------------------------------------------------------

    question: Annotated[
        str,
        Field(
            min_length=5,
            description="The evaluation question.",
        ),
    ]

    ground_truth_answer: Annotated[
        str,
        Field(
            min_length=1,
            description=(
                "The reference answer against which generated answers "
                "are compared. Required for Correctness metric."
            ),
        ),
    ]

    generation_method: GenerationMethod = Field(
        default=GenerationMethod.SYNTHETIC
    )

    # ------------------------------------------------------------------
    # RAG pipeline outputs (populated by RAGPipeline.run())
    # ------------------------------------------------------------------

    generated_answer: Annotated[
        str | None,
        Field(
            default=None,
            description=(
                "Answer generated by the RAG pipeline. "
                "None until the pair has been run through RAG."
            ),
        ),
    ]

    retrieved_chunks: Annotated[
        list[str],
        Field(
            default_factory=list,
            description=(
                "Context chunks retrieved from the vector store "
                "for this question. Populated by RAGPipeline."
            ),
        ),
    ]

    retrieved_chunk_sources: Annotated[
        list[str],
        Field(
            default_factory=list,
            description=(
                "Source file names for each retrieved chunk. "
                "Parallel list to retrieved_chunks."
            ),
        ),
    ]

    rag_latency_ms: float = Field(
        default=0.0,
        ge=0.0,
        description="Time taken by the RAG pipeline for this question.",
    )

    rag_input_tokens: int = Field(default=0, ge=0)
    rag_output_tokens: int = Field(default=0, ge=0)
    rag_model: str | None = Field(
        default=None,
        description="Model ID used for RAG answer generation.",
    )

    # ------------------------------------------------------------------
    # Evaluation outputs (populated by EvaluationEngine.evaluate())
    # ------------------------------------------------------------------

    metric_scores: Annotated[
        dict[str, MetricScore],
        Field(
            default_factory=dict,
            description=(
                "Map of metric_name → MetricScore. "
                "Populated by EvaluationEngine. "
                "Keys: faithfulness, answer_relevance, "
                "context_precision, correctness."
            ),
        ),
    ]

    composite_score: Annotated[
        float | None,
        Field(
            default=None,
            ge=1.0,
            le=5.0,
            description=(
                "Weighted average of all metric scores. "
                "None until all metrics are evaluated."
            ),
        ),
    ]

    # ------------------------------------------------------------------
    # Status and error tracking
    # ------------------------------------------------------------------

    status: QAPairStatus = Field(default=QAPairStatus.PENDING)

    error_message: str | None = Field(
        default=None,
        description="Error message if status == failed.",
    )

    # ------------------------------------------------------------------
    # Validators
    # ------------------------------------------------------------------

    @field_validator("question")
    @classmethod
    def question_not_trivial(cls, v: str) -> str:
        """Reject questions that are clearly malformed."""
        stripped = v.strip()
        if len(stripped) < 5:
            raise ValueError(
                f"Question is too short ({len(stripped)} chars). "
                f"Minimum 5 characters."
            )
        if stripped == stripped.upper() and len(stripped) > 10:
            raise ValueError(
                f"Question appears to be all-caps: '{stripped[:50]}'. "
                f"This may indicate a parsing error in the generator."
            )
        return stripped

    @field_validator("ground_truth_answer")
    @classmethod
    def ground_truth_not_empty(cls, v: str) -> str:
        stripped = v.strip()
        if not stripped:
            raise ValueError("ground_truth_answer cannot be empty.")
        return stripped

    @model_validator(mode="after")
    def evaluated_pairs_must_have_scores(self) -> QAPair:
        """
        If status is evaluated, all metric scores must be present
        and composite_score must be set.
        """
        if self.status == QAPairStatus.EVALUATED:
            required = {
                "faithfulness",
                "answer_relevance",
                "context_precision",
            }
            missing = required - set(self.metric_scores.keys())
            if missing:
                raise ValueError(
                    f"QAPair status=evaluated but missing metric scores: "
                    f"{missing}. "
                    f"All metrics except correctness (when no reference) "
                    f"must be scored."
                )
            if self.composite_score is None:
                raise ValueError(
                    "QAPair status=evaluated but composite_score is None."
                )
        return self

    # ------------------------------------------------------------------
    # Lifecycle methods
    # ------------------------------------------------------------------

    def set_answer(
        self,
        generated_answer: str,
        retrieved_chunks: list[str],
        retrieved_chunk_sources: list[str],
        latency_ms: float,
        input_tokens: int,
        output_tokens: int,
        rag_model: str,
    ) -> None:
        """
        Transition from PENDING → ANSWERED.

        Called by RAGPipeline after generating an answer.
        Validates that a pending pair is being answered.
        """
        if self.status != QAPairStatus.PENDING:
            raise ValueError(
                f"set_answer() called on QAPair with status="
                f"'{self.status.value}'. "
                f"Only PENDING pairs can be answered."
            )
        self.generated_answer = generated_answer
        self.retrieved_chunks = retrieved_chunks
        self.retrieved_chunk_sources = retrieved_chunk_sources
        self.rag_latency_ms = latency_ms
        self.rag_input_tokens = input_tokens
        self.rag_output_tokens = output_tokens
        self.rag_model = rag_model
        self.status = QAPairStatus.ANSWERED

    def set_scores(
        self,
        metric_scores: dict[str, MetricScore],
        composite_score: float,
    ) -> None:
        """
        Transition from ANSWERED → EVALUATED.

        Called by EvaluationEngine after scoring all metrics.
        """
        if self.status != QAPairStatus.ANSWERED:
            raise ValueError(
                f"set_scores() called on QAPair with status="
                f"'{self.status.value}'. "
                f"Only ANSWERED pairs can be evaluated."
            )
        self.metric_scores = metric_scores
        self.composite_score = composite_score
        self.status = QAPairStatus.EVALUATED

    def mark_failed(self, error_message: str) -> None:
        """
        Transition any non-terminal state → FAILED.

        Called when the pipeline encounters an unrecoverable error
        for this specific pair. Does not affect other pairs.
        """
        if self.status in (QAPairStatus.EVALUATED,):
            raise ValueError(
                f"Cannot mark an already-evaluated QAPair as failed."
            )
        self.status = QAPairStatus.FAILED
        self.error_message = error_message

    # ------------------------------------------------------------------
    # Computed properties
    # ------------------------------------------------------------------

    @property
    def is_evaluated(self) -> bool:
        return self.status == QAPairStatus.EVALUATED

    @property
    def is_pending(self) -> bool:
        return self.status == QAPairStatus.PENDING

    @property
    def is_failed(self) -> bool:
        return self.status == QAPairStatus.FAILED

    @property
    def has_reference(self) -> bool:
        """True if ground_truth_answer is non-empty (enables Correctness)."""
        return bool(self.ground_truth_answer.strip())

    @property
    def context_as_numbered_list(self) -> str:
        """
        Format retrieved_chunks as a numbered list for judge prompts.

        Judge prompts reference chunks by number:
        "1. <chunk text>\n2. <chunk text>\n..."
        """
        if not self.retrieved_chunks:
            return "No context retrieved."
        return "\n\n".join(
            f"{i + 1}. {chunk}"
            for i, chunk in enumerate(self.retrieved_chunks)
        )

    @property
    def faithfulness_score(self) -> float | None:
        """Convenience accessor — avoids dict lookup boilerplate."""
        ms = self.metric_scores.get("faithfulness")
        return ms.score if ms else None

    @property
    def answer_relevance_score(self) -> float | None:
        ms = self.metric_scores.get("answer_relevance")
        return ms.score if ms else None

    @property
    def context_precision_score(self) -> float | None:
        ms = self.metric_scores.get("context_precision")
        return ms.score if ms else None

    @property
    def correctness_score(self) -> float | None:
        ms = self.metric_scores.get("correctness")
        return ms.score if ms else None

    @property
    def total_eval_latency_ms(self) -> float:
        """Sum of latency_ms across all metric scores."""
        return sum(ms.latency_ms for ms in self.metric_scores.values())

    @property
    def total_eval_tokens(self) -> int:
        """Sum of input + output tokens across all metric judge calls."""
        return sum(
            ms.input_tokens + ms.output_tokens
            for ms in self.metric_scores.values()
        )

    def __repr__(self) -> str:
        return (
            f"QAPair("
            f"id='{self.id}', "
            f"status={self.status.value}, "
            f"question='{self.question[:40]}...')"
        )


