class EvalDataset(BaseModel):
    """
    Complete test dataset: metadata + list of QAPairs.

    The root object persisted by DatasetStore and consumed by
    EvaluationEngine. All pairs share the same dataset_id
    (enforced by the model validator below).

    Frozen=False because EvaluationEngine updates pairs in place
    as it processes them (set_answer, set_scores). The dataset
    object lives for the duration of an eval run.
    """

    model_config = ConfigDict(frozen=False, validate_assignment=True)

    metadata: DatasetMetadata
    pairs: Annotated[
        list[QAPair],
        Field(
            min_length=1,
            description="List of Q&A pairs. Must have at least one.",
        ),
    ]

    # ------------------------------------------------------------------
    # Validators
    # ------------------------------------------------------------------

    @model_validator(mode="after")
    def all_pairs_belong_to_this_dataset(self) -> EvalDataset:
        """Every pair's dataset_id must match metadata.id."""
        mismatched = [
            p.id
            for p in self.pairs
            if p.dataset_id != self.metadata.id
        ]
        if mismatched:
            raise ValueError(
                f"EvalDataset: {len(mismatched)} pairs have "
                f"dataset_id != '{self.metadata.id}': "
                f"{mismatched[:5]}{'...' if len(mismatched) > 5 else ''}"
            )
        return self

    @model_validator(mode="after")
    def pair_count_matches_metadata(self) -> EvalDataset:
        """
        metadata.total_pairs should match len(pairs).

        Warning only (not error) because a dataset loaded from disk
        may have been partially updated without updating metadata.
        """
        if self.metadata.total_pairs != len(self.pairs):
            import warnings
            warnings.warn(
                f"EvalDataset '{self.metadata.id}': "
                f"metadata.total_pairs={self.metadata.total_pairs} "
                f"but len(pairs)={len(self.pairs)}. "
                f"Metadata may be stale.",
                stacklevel=2,
            )
        return self

    # ------------------------------------------------------------------
    # Computed properties
    # ------------------------------------------------------------------

    @property
    def id(self) -> str:
        return self.metadata.id

    @property
    def name(self) -> str:
        return self.metadata.name

    @property
    def pending_pairs(self) -> list[QAPair]:
        return [p for p in self.pairs if p.is_pending]

    @property
    def evaluated_pairs(self) -> list[QAPair]:
        return [p for p in self.pairs if p.is_evaluated]

    @property
    def failed_pairs(self) -> list[QAPair]:
        return [p for p in self.pairs if p.is_failed]

    @property
    def answered_pairs(self) -> list[QAPair]:
        return [
            p for p in self.pairs
            if p.status == QAPairStatus.ANSWERED
        ]

    @property
    def completion_rate(self) -> float:
        """Fraction of pairs that are evaluated."""
        if not self.pairs:
            return 0.0
        return len(self.evaluated_pairs) / len(self.pairs)

    @property
    def has_any_scores(self) -> bool:
        return len(self.evaluated_pairs) > 0

    @property
    def questions(self) -> list[str]:
        """All question strings — for display in the UI."""
        return [p.question for p in self.pairs]

    # ------------------------------------------------------------------
    # Aggregate score computation
    # ------------------------------------------------------------------

    def aggregate_scores(
        self,
        weight_map: dict[str, float] | None = None,
    ) -> dict[str, float]:
        """
        Compute mean score per metric across all evaluated pairs.

        Args:
            weight_map: Optional {metric_name: weight} override.
                        If None, simple mean (not weighted) is returned.

        Returns:
            Dict with keys: faithfulness, answer_relevance,
            context_precision, correctness, composite.
            Missing metrics (e.g. correctness with no reference)
            are excluded from the dict rather than scored as 0.
        """
        evaluated = self.evaluated_pairs
        if not evaluated:
            return {}

        metric_names = [
            "faithfulness",
            "answer_relevance",
            "context_precision",
            "correctness",
        ]

        scores_by_metric: dict[str, list[float]] = {
            m: [] for m in metric_names
        }

        for pair in evaluated:
            for metric_name in metric_names:
                ms = pair.metric_scores.get(metric_name)
                if ms and not ms.parse_failed:
                    scores_by_metric[metric_name].append(ms.score)

        result: dict[str, float] = {}
        for metric_name, scores in scores_by_metric.items():
            if scores:
                result[metric_name] = round(
                    sum(scores) / len(scores), 4
                )

        # Composite score
        if result:
            if weight_map:
                # Weighted average across available metrics
                total_weight = sum(
                    weight_map.get(m, 0.0)
                    for m in result
                )
                if total_weight > 0:
                    composite = sum(
                        result[m] * weight_map.get(m, 0.0)
                        for m in result
                    ) / total_weight
                    result["composite"] = round(composite, 4)
            else:
                # Simple mean across available metrics
                result["composite"] = round(
                    sum(result.values()) / len(result), 4
                )

        return result

    def get_pair_by_id(self, pair_id: str) -> QAPair | None:
        """Return a QAPair by ID. Returns None if not found."""
        for pair in self.pairs:
            if pair.id == pair_id:
                return pair
        return None

    # ------------------------------------------------------------------
    # Serialisation
    # ------------------------------------------------------------------

    def to_json(self, indent: int = 2) -> str:
        """Serialise entire dataset to JSON string."""
        return self.model_dump_json(indent=indent)

    @classmethod
    def from_json(cls, json_str: str) -> EvalDataset:
        """Deserialise from JSON string."""
        return cls.model_validate_json(json_str)

    @classmethod
    def from_file(cls, path: Path) -> EvalDataset:
        """
        Load EvalDataset from a JSON file.

        Raises FileNotFoundError if path does not exist.
        Raises ValidationError if JSON is malformed or schema-invalid.
        """
        if not path.exists():
            raise FileNotFoundError(
                f"EvalDataset file not found: {path.resolve()}"
            )
        return cls.from_json(path.read_text(encoding="utf-8"))

    def to_file(self, path: Path) -> None:
        """
        Persist EvalDataset to a JSON file.

        Creates parent directories if they do not exist.
        Atomic write via temp file — avoids partial writes on crash.
        """
        path = path.resolve()
        path.parent.mkdir(parents=True, exist_ok=True)

        # Write to temp file first, then rename (atomic on most OSes)
        tmp_path = path.with_suffix(".tmp")
        try:
            tmp_path.write_text(
                self.to_json(),
                encoding="utf-8",
            )
            tmp_path.replace(path)
        except Exception:
            if tmp_path.exists():
                tmp_path.unlink()
            raise

    def to_csv_rows(self) -> list[dict[str, Any]]:
        """
        Convert evaluated pairs to flat dicts for CSV export.

        One row per QAPair. Metric scores are flattened into
        separate columns (faithfulness_score, faithfulness_reasoning, etc.)
        matching the csv_columns spec in eval.yaml.
        """
        rows: list[dict[str, Any]] = []

        for pair in self.pairs:
            row: dict[str, Any] = {
                "pair_id":              pair.id,
                "question":             pair.question,
                "reference_answer":     pair.ground_truth_answer,
                "generated_answer":     pair.generated_answer or "",
                "source_file":          pair.source_file,
                "source_page":          pair.source_page or "",
                "retrieved_chunks_count": len(pair.retrieved_chunks),
                "status":               pair.status.value,
                "rag_model":            pair.rag_model or "",
                "rag_latency_ms":       round(pair.rag_latency_ms, 1),
                "rag_input_tokens":     pair.rag_input_tokens,
                "rag_output_tokens":    pair.rag_output_tokens,
                "composite_score":      pair.composite_score or "",
            }

            for metric_name in [
                "faithfulness",
                "answer_relevance",
                "context_precision",
                "correctness",
            ]:
                ms = pair.metric_scores.get(metric_name)
                row[f"{metric_name}_score"] = ms.score if ms else ""
                row[f"{metric_name}_reasoning"] = (
                    ms.reasoning if ms else ""
                )
                row[f"{metric_name}_latency_ms"] = (
                    round(ms.latency_ms, 1) if ms else ""
                )
                row[f"{metric_name}_parse_failed"] = (
                    ms.parse_failed if ms else ""
                )

            rows.append(row)

        return rows

    def __repr__(self) -> str:
        return (
            f"EvalDataset("
            f"id='{self.id}', "
            f"name='{self.name}', "
            f"pairs={len(self.pairs)}, "
            f"evaluated={len(self.evaluated_pairs)}, "
            f"completion={self.completion_rate:.0%})"
        )


